def _make_synthetic_hpc(seed: int, n_train: int = 240, n_test: int = 160):
    rng = np.random.default_rng(seed)
    n_features = 8
    x_train_benign = rng.normal(0.0, 1.0, size=(n_train // 2, n_features))
    x_train_attack = rng.normal(0.0, 1.0, size=(n_train // 2, n_features))
    x_train_attack[:, 2:5] += rng.normal(2.0, 0.25, size=(n_train // 2, 3))
    x_train_attack[:, 6] += rng.normal(1.4, 0.2, size=(n_train // 2,))
    x_train = np.vstack([x_train_benign, x_train_attack])
    y_train = np.array([0] * (n_train // 2) + [1] * (n_train // 2), dtype=int)

    x_test_benign = rng.normal(0.0, 1.0, size=(n_test // 2, n_features))
    x_test_attack = rng.normal(0.0, 1.0, size=(n_test // 2, n_features))
    x_test_attack[:, 2:5] += rng.normal(1.7, 0.35, size=(n_test // 2, 3))
    x_test_attack[:, 6] += rng.normal(1.1, 0.25, size=(n_test // 2,))
    x_test = np.vstack([x_test_benign, x_test_attack])
    y_test = np.array([0] * (n_test // 2) + [1] * (n_test // 2), dtype=int)

    perm_train = rng.permutation(len(y_train))
    perm_test = rng.permutation(len(y_test))
    return x_train[perm_train], y_train[perm_train], x_test[perm_test], y_test[perm_test]


def _as_binary_predictions(values, expected_len: int):
    arr = np.asarray(values)
    if arr.shape[0] != expected_len:
        raise ValueError(f"predict returned {{arr.shape[0]}} rows; expected {{expected_len}}")
    if arr.ndim > 1:
        arr = arr.reshape((expected_len, -1))[:, 0]
    arr = np.where(arr.astype(float) >= 0.5, 1, 0).astype(int)
    if not np.all(np.isfinite(arr)):
        raise ValueError("predict returned non-finite values")
    return arr


def _score(y_true, y_pred):
    y_true = np.asarray(y_true).astype(int)
    y_pred = np.asarray(y_pred).astype(int)
    tp = int(np.sum((y_true == 1) & (y_pred == 1)))
    tn = int(np.sum((y_true == 0) & (y_pred == 0)))
    fp = int(np.sum((y_true == 0) & (y_pred == 1)))
    fn = int(np.sum((y_true == 1) & (y_pred == 0)))
    precision = tp / max(tp + fp, 1)
    recall = tp / max(tp + fn, 1)
    f1 = 2 * precision * recall / max(precision + recall, 1e-12)
    tpr = recall
    tnr = tn / max(tn + fp, 1)
    fpr = fp / max(fp + tn, 1)
    accuracy = (tp + tn) / max(len(y_true), 1)
    return {{
        "accuracy": float(accuracy),
        "detection_f1": float(f1),
        "precision": float(precision),
        "tpr": float(tpr),
        "tnr": float(tnr),
        "fpr": float(fpr),
    }}


