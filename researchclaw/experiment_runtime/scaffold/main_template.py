_SCAFFOLD_DIR = Path(__file__).resolve().parent


def scaffold_sha256() -> str:
    """Return a deterministic digest over all runtime-owned scaffold files."""
    h = hashlib.sha256()
    for path in sorted(_SCAFFOLD_DIR.glob("*.py")):
        if path.name == "__init__.py":
            continue
        rel = path.relative_to(_SCAFFOLD_DIR).as_posix()
        data = path.read_bytes()
        h.update(rel.encode("utf-8"))
        h.update(b"\0")
        h.update(str(len(data)).encode("ascii"))
        h.update(b"\0")
        h.update(data)
        h.update(b"\0")
    return h.hexdigest()


